package com.example.els.models;

public class Achievement {
    private int image;
    private String name;

    Achievement() {

    }

    public Achievement(int image, String name) {
        this.image = image;
        this.name = name;
    }

    public int getImage() {
        return image;
    }

    public void setImage(int image) {
        this.image = image;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
